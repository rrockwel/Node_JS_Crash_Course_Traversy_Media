const os = require('os');


/*************************************************************
* Platform 
*************************************************************/

console.log(os.platform());

/*************************************************************
* CPU Architecture
**************************************************************/

console.log(os.arch());

/*************************************************************
* CPU Core Infor
**************************************************************/

console.log(os.cpus());

/*************************************************************
* Free Memory
**************************************************************/

console.log('Free Memory: ' + os.freemem());

/*************************************************************
* Total Memory
**************************************************************/

console.log('Total Memory: ' + os.totalmem());

/*************************************************************
* Home Directory
**************************************************************/

console.log('Home Directoy: ' + os.homedir());

/*************************************************************
* System Uptime
**************************************************************/

console.log('System Uptime: ' + ((os.uptime())/60/60) + ' hours');
